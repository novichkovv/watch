    <h2>Безопасность оплаты</h2>
    <p>Безопасность платежей с помощью банковских карт обеспечивается технологиями защищенного соединения HTTPS и двухфакторной аутентификации пользователя 3D Secure.

        <br>В соответствии с ФЗ «О защите прав потребителей» в случае, если вам была оказана услуга или реализован товар ненадлежащего качества, платеж может быть возвращен на банковскую карту, с которой производилась оплата. Порядок возврата средств уточняйте у администрации интернет-магазина.</p>
    <h2>Информация о способах оплаты</h2>
    <p><strong>Наличный расчёт</strong></p>
    <p>Вы можете оплатить заказ наличными курьеру при его получении или в пункте самовывоза.
        При доставке почтой России оплата может осуществляться наложенным платежом.
        <br>При получении товара обязательно проверьте комплектацию заказа, наличие гарантийного талона и чека.</p>


